package examples;

public class Exampleswitch {

	public static void main(String[] args) {
		String j="Two";
		switch(j) {
		case "zero":
		System.out.println("value is zero");
		break;
		case "one":
		System.out.println("value is one");
		break;
		case "Two":
		System.out.println("value is two");
		break;
		default:
		System.out.println("no value");
		break;
		}

	}

}
